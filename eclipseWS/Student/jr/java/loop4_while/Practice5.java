public class Practice5 {

	public static void main(String[] args) {
		
		int aaa;
		int i = 1;
		
		while(i<10){
			i++;
			aaa = i;
		}
		//System.out.println(aaa);
		
		int bbb;
		int j = 1;
		
		do{
			j++;
			bbb = j;
		}while(j<10);
		System.out.println(bbb);
	}

}
