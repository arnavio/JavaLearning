package jrJava.loopString;

public class Practice1 {


	public static void main(String[] args) {

		for(int i=0; i<4; i++){

			System.out.println("Welcome!");

			for(int j=0; j<3; j++){
				System.out.println(i + "," + j);
			}

			System.out.println("Good Bye!");
			System.out.println();

		}

		System.out.println("We're done!");
	}

}
